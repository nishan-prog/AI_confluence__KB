// Force Node to use OpenSSL legacy provider before any imports
process.env.NODE_OPTIONS = '--openssl-legacy-provider';

import express from "express";
import { google } from "googleapis";
import dotenv from "dotenv";
import axios from "axios";
import fs from "fs";

dotenv.config();

const app = express();
app.use(express.json());

const PORT = process.env.PORT || 3000;

// ---- Constants ----
const POLL_INTERVAL = 60 * 1000; // 1 min
let reviewQueue = [];             // Tickets waiting for review
const STATE_FILE = "./state.json";
let state = {};

// ---- Load state.json ----
if (fs.existsSync(STATE_FILE)) {
  try {
    state = JSON.parse(fs.readFileSync(STATE_FILE, "utf8"));
  } catch (err) {
    console.error("‚ùå Failed to parse state.json:", err.message);
    state = {};
  }
}

// ---- Save state helper ----
function saveState() {
  fs.writeFileSync(STATE_FILE, JSON.stringify(state, null, 2));
}

// ---- Gmail API Auth ----
let serviceAccount;
try {
  serviceAccount = JSON.parse(process.env.GOOGLE_SERVICE_JSON);
} catch (err) {
  console.error("‚ùå Invalid JSON in GOOGLE_SERVICE_JSON:", err);
  process.exit(1);
}

const auth = new google.auth.JWT({
  email: serviceAccount.client_email,
  key: serviceAccount.private_key,
  scopes: [
    "https://www.googleapis.com/auth/gmail.readonly",
    "https://www.googleapis.com/auth/gmail.send",
  ],
  subject: "support@stoneandchalk.com.au",
});

const gmail = google.gmail({ version: "v1", auth });

// ---- Confluence API setup ----
const CONFLUENCE_BASE_URL = process.env.CONFLUENCE_SITE;
const CONFLUENCE_API_KEY = process.env.CONFLUENCE_API_KEY;
const CONFLUENCE_USER = process.env.CONFLUENCE_USER;
const CONFLUENCE_SPACE = process.env.CONFLUENCE_SPACE;

// ---- Jira API setup ----
const JIRA_BASE_URL = process.env.JIRA_SITE;
const JIRA_API_TOKEN = process.env.JIRA_API_TOKEN;
const JIRA_USER_EMAIL = process.env.JIRA_USER_EMAIL;

// ---- Poll Jira for recently resolved tickets ----
async function pollResolvedTickets() {
  try {
    const lastPoll = state.lastPollTimestamp
      ? new Date(state.lastPollTimestamp).toISOString()
      : new Date(Date.now() - 60 * 60 * 1000).toISOString(); // fallback: 1 hour ago

    // Jira JQL: tickets resolved since last poll
    const jql = `status = Resolved AND resolved >= "${lastPoll}"`;

    const res = await axios.get(`${JIRA_BASE_URL}/rest/api/3/search`, {
      auth: { username: JIRA_USER_EMAIL, password: JIRA_API_TOKEN },
      params: { jql, fields: "summary,description,assignee,resolutiondate" },
    });

    const issues = res.data.issues;
    if (!issues || issues.length === 0) {
      console.log("üì≠ No recently resolved Jira tickets found.");
    }

    for (const issue of issues) {
      const { key, fields } = issue;
      const status = fields.status.name;
      const assigneeEmail = fields.assignee?.emailAddress;
      const resolvedTimestamp = fields.resolutiondate
        ? new Date(fields.resolutiondate).getTime()
        : 0;

      if (!assigneeEmail) continue;

      // Prepare Gemini summary placeholder (can replace with real Gemini summary)
      const summary = `Gemini Summary for ticket ${key}: ${fields.summary}\n\n${fields.description || ""}`;

      // Add to review queue
      reviewQueue.push({ subject: `${key} - ${fields.summary}`, body: summary });

      console.log(`üìù Ticket added to review queue: ${key} - ${fields.summary}`);

      // Send review email to assignee
      await sendReviewEmail(assigneeEmail, `${key} - ${fields.summary}`, summary);
    }

    // Update last poll timestamp
    state.lastPollTimestamp = Date.now();
    saveState();

  } catch (err) {
    console.error("üö® Error polling Jira tickets:", err.response?.data || err.message);
  }
}

// ---- Send review email ----
async function sendReviewEmail(assigneeEmail, subject, summary) {
  try {
    const rawMessage = [
      `From: support@stoneandchalk.com.au`,
      `To: ${assigneeEmail}`,
      `Subject: Review Required: ${subject}`,
      `Content-Type: text/html; charset=UTF-8`,
      "",
      `<p>Hi,</p>
      <p>A new resolved Jira ticket summary generated by Gemini is ready for review:</p>
      <p><strong>${summary}</strong></p>
      <p>Approve this to post to Confluence.</p>`
    ].join("\n");

    const encodedMessage = Buffer.from(rawMessage)
      .toString("base64")
      .replace(/\+/g, '-')
      .replace(/\//g, '_')
      .replace(/=+$/, '');

    await gmail.users.messages.send({
      userId: "support@stoneandchalk.com.au",
      requestBody: { raw: encodedMessage },
    });

    console.log(`‚úâÔ∏è Review email sent to: ${assigneeEmail}`);
  } catch (err) {
    console.error("üö® Failed to send review email:", err.response?.data || err.message);
  }
}

// ---- Post approved summaries to Confluence ----
async function postToConfluence() {
  if (reviewQueue.length === 0) {
    console.log("üì≠ No items in review queue.");
    return;
  }

  for (const item of reviewQueue) {
    try {
      await axios.post(
        `${CONFLUENCE_BASE_URL}/rest/api/content/`,
        {
          type: "page",
          title: `KB Draft - ${item.subject}`,
          space: { key: CONFLUENCE_SPACE },
          body: { storage: { value: `<p>${item.body}</p>`, representation: "storage" } },
        },
        { auth: { username: CONFLUENCE_USER, password: CONFLUENCE_API_KEY }, headers: { "Content-Type": "application/json" } }
      );
      console.log(`‚úÖ Confluence draft created for: ${item.subject}`);
    } catch (err) {
      console.error(`üö® Failed to create Confluence page for: ${item.subject}`, err.response?.data || err.message);
    }
  }

  reviewQueue = [];
}

// ---- Intervals ----
setInterval(pollResolvedTickets, POLL_INTERVAL);

// ---- Manual endpoint to post review queue ----
app.post("/review/post", async (req, res) => {
  await postToConfluence();
  res.send("‚úÖ Review queue posted to Confluence.");
});

app.get("/", (req, res) => {
  res.send("üöÄ AI KB Draft Bot Running and Polling Jira");
});

app.listen(PORT, () => console.log(`Server running on port ${PORT}`));
